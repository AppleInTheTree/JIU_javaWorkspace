package jiu_java_prepare;

public class WhileLoopStatement {
    public static void main(String[] args) {

        /*
         * while(조건){
         *   반복 실행 영역
         * }
         * */
        int i  = 0;
        while ( i <= 10){
            System.out.println(i);
            i++;
        }
    }

}
