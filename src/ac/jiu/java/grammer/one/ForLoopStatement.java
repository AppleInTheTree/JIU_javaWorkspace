package ac.jiu.java.grammer.one;

public class ForLoopStatement {
    /*
    * for(초기화; 종료조건; 반복실행){
    *   반복적으로 실행될 구문
    * }
    * */

    public static void main(String[] args) {
        int a;
        for (a = 0; a <= 10 ; a++){
            System.out.println("Count 0 to 10 : " + a);
        }
    }
}
